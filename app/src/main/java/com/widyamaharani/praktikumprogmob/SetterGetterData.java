package com.widyamaharani.praktikumprogmob;

public class SetterGetterData {
    private int id;
    private String kategori;
    private int uang;
    private String deskripsi;
    private String tanggal;
    private int prioritas;

    public int getId() {return id;}

    public void setId(int id) {this.id = id;}

    public String getKategori() {return kategori;}

    public void setKategori(String kategori) {this.kategori = kategori;}

    public int getUang() {return uang;}

    public void setUang(int uang) {this.uang = uang;}

    public String getDeskripsi() {return deskripsi;}

    public void setDeskripsi(String deskripsi) {this.deskripsi = deskripsi;}

    public String getTanggal() {return tanggal;}

    public void setTanggal(String tanggal) {this.tanggal = tanggal;}

    public int getPrioritas() {return prioritas;}

    public void setPrioritas(int prioritas) {this.prioritas = prioritas;}
}
